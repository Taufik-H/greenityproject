        this.gambar = gambar;
        this.subject = subject;
        this.tanggal = tanggal;
        this.hariRaya = hariRaya;
        this.ucapan = ucapan;
        this.username = username;
        this.userId = userId;
        this.type = type;
        this.websiteUrl = websiteUrl;
